function aplicarIdioma(idioma) {
  const idiomaEscolhido = traducoes[idioma] ? idioma : "pt-BR";

  document.querySelectorAll("[data-i18n]").forEach(elemento => {
    const chave = elemento.dataset.i18n;
    const textoTraduzido = traducoes[idiomaEscolhido][chave];

    if (textoTraduzido !== undefined) {
      elemento.textContent = textoTraduzido;
    }
  });

  document.documentElement.lang = idiomaEscolhido;

  document.querySelectorAll("[data-language]").forEach(botao => {
    const estaAtivo = botao.dataset.language === idiomaEscolhido;

    botao.classList.toggle("active", estaAtivo);
    botao.setAttribute("aria-pressed", estaAtivo);
  });

  localStorage.setItem("idiomaCasamento", idiomaEscolhido);
}

document.querySelectorAll("[data-language]").forEach(botao => {
  botao.addEventListener("click", () => {
    aplicarIdioma(botao.dataset.language);
  });
});

const idiomaSalvo = localStorage.getItem("idiomaCasamento");

const idiomaInicial =
  idiomaSalvo ||
  (navigator.language.toLowerCase().startsWith("pt")
    ? "pt-BR"
    : "en");

aplicarIdioma(idiomaInicial);